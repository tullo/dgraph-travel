// Success and failure markers.
const (
	success = "\u2713"
	failed  = "\u2717"
)

// TestPlaces validates searches can be conducted against the Google maps API.
func TestPlaces(t *testing.T) {
	t.Log("Given the need to retreve places.")
	{
		testID := 0
		t.Logf("\tTest %d:\tWhen handling a single city.", testID)
		{
			var client mockSearcher

			filter := places.Filter{
				Name:    "Sydney",
				Lat:     -33.865143,
				Lng:     151.209900,
				Keyword: "hotels",
				Radius:  5000,
			}

			var savePlace string
			for i := 0; i < 2; i++ {
				places, err := places.Search(context.Background(), client, &filter)
				if err != nil {
					t.Fatalf("\t%s\tTest %d:\tShould be able to search for places : %v", failed, testID, err)
				}
				t.Logf("\t%s\tTest %d:\tShould be able to search for places.", success, testID)

				exp := 20
				if len(places) != 20 {
					t.Errorf("\t%s\tTest %d:\tShould get a full page of places : %v", failed, testID, err)
					t.Logf("\t\tTest %d:\tGot: %v", testID, len(places))
					t.Logf("\t\tTest %d:\tExp: %v", testID, exp)
				} else {
					t.Logf("\t%s\tTest %d:\tShould get a full page of places.", success, testID)
				}

				if savePlace == places[i].Name {
					t.Errorf("\t%s\tTest %d:\tShould get different places per page.", failed, testID)
				} else {
					t.Logf("\t%s\tTest %d:\tShould get different places per page.", success, testID)
				}
				savePlace = places[0].Name
			}
		}
	}
}

type mockSearcher struct {
	result int
}

// NearbySearch implements the NearbySearcher interface.
func (s mockSearcher) NearbySearch(ctx context.Context, r *maps.NearbySearchRequest) (maps.PlacesSearchResponse, error) {
	var response maps.PlacesSearchResponse
	if err := json.Unmarshal([]byte(result[s.result]), &response); err != nil {
		return maps.PlacesSearchResponse{}, err
	}
	s.result++
	return response, nil
}

